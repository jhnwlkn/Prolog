% Question
recommend_movie :-
    write('Apa genre film yang Anda inginkan? '), nl,
    read(Genre),
    write('Siapa aktor yang Anda sukai? '), nl,
    read(Actor),
    write('Apakah Anda ingin alur cerita yang rumit? (yes/no) '), nl,
    read(Plot),
    write('Apakah Anda menginginkan editing yang unik? (yes/no) '), nl,
    read(Editing),
    find_movie(Genre, Actor, Plot, Editing, Movie),
    write('Berdasarkan preferensi Anda, saya merekomendasikan film: '), write(Movie), nl.

% Search
find_movie(Genre, Actor, Plot, Editing, Movie) :-
    film(Movie, Genres, Actors, Plots, Editings),
    match_genre(Genre, Genres),
    match_actor(Actor, Actors),
    match_plot(Plot, Plots),
    match_editing(Editing, Editings).

% Matching genre
match_genre(Genre, Genres) :-
    member(Genre, Genres).

% Matching actor
match_actor(Actor, Actors) :-
    member(Actor, Actors).

% Matching plot
match_plot(yes, _).

% Matching edit
match_editing(yes, _).